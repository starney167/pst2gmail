    private void btnTestLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTestLoginActionPerformed
        // we need to test to see if we can't connect to the IMAP server...
        transfer.setCredentials(txtGmailLogin.getText(), new String(txtGmailPassword.getPassword()));
        // try and connect
        try {
            this.transfer.connect();
            lblGmailConnectionStatus.setText("Connected!");
            Vector<IMAPFolderSelectItem> folders = transfer.getIMAPFolders();
            comboIMAPTo.setModel(new javax.swing.DefaultComboBoxModel(folders));
            //comboIMAPTo.setSelectedItem(transfer.getInbox());
        } catch (MessagingException err) {
            lblGmailConnectionStatus.setText(err.toString());
        }
        this.btnStart.setEnabled(this.transfer.canTransfer());
    }//GEN-LAST:event_btnTestLoginActionPerformed

    private void btnPSTFileBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPSTFileBrowseActionPerformed

        // get the PST File
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter pstExt = new FileNameExtensionFilter("PST Files", "pst");
        chooser.setFileFilter(pstExt);
        int returnVal = chooser.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            // we selected a file, attempt to open it
            File theFile = chooser.getSelectedFile();
            if (!chooser.getSelectedFile().canRead()) {
                lblPSTFileName.setText("Unable to open file for reading!");
            }
            lblPSTFileName.setText(theFile.getName());
            // attempt to open the PST file
            try {
                txtPstFileDetails.setText("Loading "+theFile.getName()+"...");
                this.repaint();
                transfer.openPST(theFile);
                txtPstFileDetails.setText(transfer.getPSTDetails());
                Vector folders = transfer.getPSTFolders();
                comboFromEmailFolders.setModel(new javax.swing.DefaultComboBoxModel(folders));
                comboFromEmailFolders.setSelectedItem(transfer.getInbox());
                comboFromContactsFolder.setModel(new javax.swing.DefaultComboBoxModel(folders));
                comboFromContactsFolder.setSelectedItem(transfer.getContacts());
                comboFromCalendarFolder.setModel(new javax.swing.DefaultComboBoxModel(folders));
                comboFromCalendarFolder.setSelectedItem(transfer.getCalendar());
            } catch (Exception err) {
                txtPstFileDetails.setText(err.toString());
            }
        }
        this.btnStart.setEnabled(this.transfer.canTransfer());

    }//GEN-LAST:event_btnPSTFileBrowseActionPerformed

