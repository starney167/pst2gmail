    private void btnStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartActionPerformed
        // TODO add your handling code here:
        // we can start the transfer!!!
        PSTFolderSelectItem from = (PSTFolderSelectItem)this.comboFromEmailFolders.getSelectedItem();
        IMAPFolderSelectItem to = (IMAPFolderSelectItem)this.comboIMAPTo.getSelectedItem();

        if (chkIncludeSubfolders.isSelected()) {
            this.transfer.includeSubFolders();
        }
        this.transfer.setFolderSelections(from, to);

        // show the status window...
        StatusWindow status = new StatusWindow(this, this.transfer);
        status.setVisible(true);

        // don't go clicking twice!
        btnStart.setEnabled(false);

    }//GEN-LAST:event_btnStartActionPerformed

